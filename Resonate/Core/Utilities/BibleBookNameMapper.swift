        case "NUM": return "Numbers"
        case "DEU": return "Deuteronomy"
        case "JOS": return "Joshua"
        case "JDG": return "Judges"
        case "RUT": return "Ruth"
        case "1SA": return "1 Samuel"
        case "2SA": return "2 Samuel"
