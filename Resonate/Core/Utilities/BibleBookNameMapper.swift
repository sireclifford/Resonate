        case "2PE": return "2 Peter"
        case "1JN": return "1 John"
        case "2JN": return "2 John"
        case "3JN": return "3 John"
        case "JUD": return "Jude"
        case "REV": return "Revelation"
            
        default:
            return usfm
        }
    }
}
